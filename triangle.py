def area(a, h): 
    '''
    Принимает числа a и h, возвращает площадь треугольника со стороной a и высотой h
    input:
        10 12
    output:
        60.0
    '''
    return a * h / 2 

def perimeter(a, b, c): 
    '''
    Принимает числа a, b и с, возвращает периметр треугольника со сторонами a, b и с
    input:
        1 2 3
    output:
        6
    '''
    return a + b + c 